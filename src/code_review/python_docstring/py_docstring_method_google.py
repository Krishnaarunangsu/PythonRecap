def add(num_1:int, num_2:int)->int:
    """ Adds two integers and returns an integer

    Args:
        num_1: int
                The First Number
        num_2: int
                Second Number

    Returns:
        sum_tot: int
                    Returns the sum of the above two numbers

    """
    sum_tot=num_1+num_2
    return sum_tot